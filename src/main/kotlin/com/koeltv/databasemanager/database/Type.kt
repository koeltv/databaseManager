package com.koeltv.databasemanager.database

enum class Type {
    // Numeric (boolean)
    BIT,
    BOOLEAN,
    // Numeric (integer)
    TINYINT,
    SMALLINT,
    INTEGER,
    BIGINT,
    NUMERIC,
    // Numeric (floating point)
    DECIMAL,
    REAL,
    DOUBLE,
    FLOAT,
    // String
    CHAR,
    VARCHAR,
    VARBINARY,
    BLOB,
    BINARY,
    // Date and time
    TIMESTAMP,
    DATE,
}